package org.firstinspires.ftc.teamcode.Outreach.Vision;

public class PipelineResults {

    public double tx = 0;
    public double ty = 0;
    public double ta = 0;
    public int pipeline = 0;

    public PipelineResults(double tx, double ty, double ta, int pipeline) {
        this.tx = tx;
        this.ty = ty;
        this.ta = ta;
        this.pipeline = pipeline;
    }

}
